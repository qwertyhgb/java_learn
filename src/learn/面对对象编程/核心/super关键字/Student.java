package learn.面对对象编程.核心.super关键字;

public class Student {
    int number;
    String name;
    Student() {
    }
    Student(int number, String name) {
        this.number = number;
        this.name = name;
    }
    public int getNumber() {
        return number;
    }
    public String getName() {
        return name;
    }
}
